using System;

namespace Smart.Windows.Mvc
{
	/// <summary>
	/// ビューイベントサポート
	/// </summary>
	public interface IViewEventSupport
	{
		/// <summary>
		/// ビューオープン
		/// </summary>
		/// <param name="args"></param>
		void OnViewOpen(ViewForwardEventArgs args);

		/// <summary>
		/// ビュー活性化
		/// </summary>
		/// <param name="args"></param>
		void OnViewActived(ViewForwardEventArgs args);

		/// <summary>
		/// ビュークローズ
		/// </summary>
		void OnViewClose();
	}
}